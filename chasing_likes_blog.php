				<div id="blog_template_content_div">
					<p>Have you ever tried marketing your business on Facebook and the only goal you set yourself is to achieve thousands of likes? Is that a yes I hear? Keep reading, as this article will change your mind. There’s a misconception about social media marketing: the more likes you have, the better you - cough-cough - I mean, your account, is. It's true, to some extent. Your followers do a good job of proving your credibility. However, like chasers will drive businesses away from their core digital marketing objectives. People get carried away when trying to achieve social media success; they chase likes instead of real connections.</p>
					<br>
					<h3><b><i>Here’s the truth. “Likes" don’t necessarily translate to engagement, let alone sales. For business success, you need engagement and conversions</i></b></h3>
					<br>
					<p>Why?</p>
					<br>
					<p>A “like” doesn’t translate to an actual liking to your brand, product or business. You’ll be amazed why people hit 'like':</p>
					<br>
					<p>* Someone likes a post just because their friends tag them;
					<br>* They will like a comment, so they don’t have to reply to it - a quick way of saying “I’m done, let’s stop.”
					<br>* People can hit "like" just because they are bored - I mean, it's fun and easy, we have all done it.
					<br>* Some friends just expect you to show some interactions. Giving them the digital thumbs up means you don't need to call them. Let's be honest, social media makes it so easy to interact with others.
					<br>* Sometimes you “like” hoping people will “like” you back. 
					<br>*And lastly, we also need to take into account the automatic robots that auto-like or follow, in the hope you will like or follow them back.</p>
					<br>
					<p>We refer to all these as ‘blind likes’; no action follows the like. They prove one thing - many of us “like” as it requires little effort and helps avoid having to write a proper comment. It becomes an unhealthy way to interact and is bad for business. What you need is meaningful social media engagement, either at a personal level or from a business perspective. Blind likes don't give you that.</p>
					<br>
					<h3><b><i>Let’s sort out the priorities here. Marketing for small businesses starts with making a profit and ends with making a profit. Conversion is all that matters.</i></b></h3>
					<br>
					<p>What is a conversion? In simple words, conversion means getting your audience to perform the desired action towards a marketing message. For example, you may want them to fill in an online form, sign up for your monthly newsletter, follow the link in the email, call the number on your business card, subscribe to your blog, etc.</p>
					<br>
					<p>Any of the above can take your customers a step closer to the biggest conversion - purchasing your products, a.k.a. SALES. Sales are what feed your business and keep it running. Leave fluffy marketing messages to those who have large marketing budgets.</p>
					<br>
					<p>Now you may realise that it’s time to rethink your social media marketing objectives, especially if your goal has always been to chase blind likes.</p>
					<br>
					<p>What makes good marketing objectives?</p>
					<br>
					<p>First thing first, you start with a goal that gives results. Use original content and don’t fall for the trend craze. Remember the “follow me” photos from that famous photographer holding his girlfriend’s hand to different places around the world? There was a tremendous amount of “shares.” Some even started recreating the same photos.  If your social media is the same-same; it will bore the hell out of your audience. They keep seeing your content on their friend’s feeds and don’t need to see it again from you.</p>
					<br>
					<p>Customers need a reason to come to you instead of your competitors and that’s why you need to differentiate your values. It doesn’t have to be too complicated, just be yourself. Your posts should be written like you are having a face-to-face conversations with your customers. Make your content relevant. If you are a cafe owner, talk about how a cup of coffee can make a great start to the day.</p>
					<br>
					<p>Creating original content and having an authentic voice require a plan. Be it a Facebook marketing campaign or an Instagram strategy, it all starts with the basics. Go through the list of your products and services, work out who would use them so you can identify your target customers, research on their behaviour, spending habits, etc. From there, you will have a better idea what customers want to see and how they want to see it, so you can design a strategy to deliver your message.</p>
					<br>
					<p>Good marketing objectives are those that help define the ultimate goals of your business and guide you how to get there. Remember that the marketing messages should resonate with your customers. So stop chasing likes and start marketing the right way for small business today.</p>
