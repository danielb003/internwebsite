					<p>You can't go wrong with traditional promotional methods such as word of mouth. However, in an ever changing world, technology and social media have become such an integral part of our daily lives - and utilised correctly, they can be the most efficient and effective way to promote your business.</p>
					<br>
					<h3>1. Back To Basics With Free Advertising</h3>
					<p>Promote your business at every oppurtunity you get, whether that be handing out business cards, attaching your business name, logo and contact information to your email signature or wrapping your car with your business information. It is a subtle and effective way to create awareness about your business.</p>
					<br>
					<h3>2. Utilise Social Media</h3>
					<p>Today, the influence of social media is unparalleled; Facebook, Twitter, and LinkedIn are just a few useful platforms to promote your business. Surprisingly, many small businesses don't use social media as a marketing tool.  It can be very cost effective.  As you gain popularity and a loyal following by posting your awareness on social media and thus, your marketing, will be boosted.</p>
					<br>
					<h3>3.  Participate in online group discussions.</h3>
					<p>Use Facebook comments, LinkedIn blog posts or Twitter threads to engage with people and consequently increase followers. Online discussions and forum posts on topics that promote the message of your business will gain the attention of interested consumers.</p>
					<br>
					<h3>4. Online email service</h3>
					<p>Create a client email list and send out an online newsletter or promotional offers to customers to promote your business. A signup discount on products or services could act as an incentive to have more people signing up to your email list and translate to more sales.</p>
					<br>
					<h3>5. Publicity from the press</h3>
					<p>Issue media releases to newspapers, radio stations and magazines, whose readers will notice your business. If you get a mention in a news article, magazine or blog, it validates your brand.  It means new customers will trust you. It doesn't end there, use the piece to publicise your business by reposting, reprinting or linking published articles and use them as marketing tools to lend credibility to your brand.</p>
					<br>
					<h3>6. Videos</h3>
					<p>The influence of videos on YouTube or Facebook has become recognised as a simple and commonly used way of communicating and connecting with consumers. According to Get Noticed Digital Agency’s video producer Liam Morkham, “video is becoming much more than just a tool for entertainment or information. It has evolved into a communication tool that has become one of the most versatile and accessible digital mediums.”</p>
					<br>
					<p>Upload videos of your product or service, as well as introductory videos on your business and yourself to gain trust from your audience of potential customers through interaction.</p>
					<br>
					<h3>7. Contests</h3>
					<p>Organise a contest to increase awareness for your business. Give prizes that are desirable and related to your business to directly promote it - this may include a gift basket of your products or a discount on your services. The Sensis Social Media report from 2017 found that those who do follow businesses were increasingly after discounts (54%) and giveaways (48%).</p>
					<br>
					<h3>8. Blogs and articles</h3>
					<p>Writing and publishing pieces about your business expertise and knowledge for aspiring entrepreneurs is a great way to lend yourself some credibility and expert status, so consumers are more likely to trust you and your business.</p>
					<br>
					<h3>9. Freebies</h3>
					<p>Consumers love free products, and handing out valuable rewards that promote your business and the products and services you offer will increase awareness and bring in new customers. You don’t necessarily have to give away your valuable products for free – offer a freebie when they spend over a certain amount or buy a particular product.</p>
					<br>
					<p>In today’s digital market, the tools and tricks above have the potential to boost your business from start-up to success story. You don’t need an absurd amount of money or a marketing background to promote your brand, as long as you learn to utilise the effectiveness of social media to help your business grow.</p>
